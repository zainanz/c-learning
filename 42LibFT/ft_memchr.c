#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((int *)str)[i] == c)
			return (&((int *)str)[i]);
		i++;
	}
	return (0);
}
